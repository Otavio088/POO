    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        Conta c = new Conta();
        MetodosContas s = new MetodosContas();
        boolean test = false;
        
        for (int i = 0; i < listaContas.size(); i++) {
            if (Integer.parseInt(txtnConta.getText()) == listaContas.get(i).getnConta()) {
                JOptionPane.showMessageDialog(null,"NÃ£o foi possÃ­vel realizar o cadastro,\nNÂ° de Conta jÃ¡ Existente!",
                       "Erro",1);
                test = true;
                break;
            }
        }    
        
        if (test == false) {
            c.setnConta(Integer.parseInt(txtnConta.getText()));
            c.setNomeCliente(txtNomeCliente.getText());
            c.setCPF(txtCPF.getText());
            c.setTelefone(txtTelefone.getText());
            c.setCidade(txtCidade.getText());
            c.setEstado(txtEstado.getText());
            c.setSaldo(Float.parseFloat(txtSaldo.getText()));
            
            s.salvar(c);
        }
        
        limpar();
    }//GEN-LAST:event_btnCadastrarActionPerformed


