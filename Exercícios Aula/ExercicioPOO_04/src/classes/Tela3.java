    private void btnBuscaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscaActionPerformed
        modelo = (DefaultTableModel) tabela.getModel();
        modelo.setRowCount(0); //eliminando as linhas da tabela
        
        for (int i = 0; i < DadosProduto.vetProd.size(); i++) { //objeto declarado na classe DadosProduto
            //criando um array de objetos e adicionando este vetor na linha do modelo
            if (comboBox.getSelectedItem() == DadosProduto.vetProd.get(i).getTipo()) {
            modelo.addRow(new Object[]{
                DadosProduto.vetProd.get(i).getDesc(),
                DadosProduto.vetProd.get(i).getTipo(),
                DadosProduto.vetProd.get(i).getPrecUnit(),
                DadosProduto.vetProd.get(i).getQtd()
            });
            }
        }
    }//GEN-LAST:event_btnBuscaActionPerformed

