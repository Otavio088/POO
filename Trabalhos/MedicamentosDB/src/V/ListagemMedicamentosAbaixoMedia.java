    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);

        MedicamentoC med = new MedicamentoC();
        
        Double mediaMedicamento = med.calcularMedia();
        txtMedia.setText(Double.toString(mediaMedicamento));
        
        listaMedicamentos = med.buscar(mediaMedicamento); // busca

        for (int i = 0; i < listaMedicamentos.size();i++) {
             modelo.addRow(new Object[]{
                 listaMedicamentos.get(i).getNome(),
             });
         }  
    }//GEN-LAST:event_btnListarActionPerformed


